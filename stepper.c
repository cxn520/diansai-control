#include "stepper.h"

/***************************************************************************
函数功能：初始化所有步进电机（上电使能）
入口参数：无
返回值  ：无
***************************************************************************/
void Stepper_Init(void)
{
    Stepper_Enable(STEPPER_ADDR_CHASSIS);
    Stepper_Enable(STEPPER_ADDR_LIFT);
}

/***************************************************************************
函数功能：使能指定地址的步进电机
入口参数：addr - 电机地址
返回值  ：无
***************************************************************************/
void Stepper_Enable(uint8_t addr)
{
    Emm_V5_En_Control(addr, true, false);
}

/***************************************************************************
函数功能：失能指定地址的步进电机
入口参数：addr - 电机地址
返回值  ：无
***************************************************************************/
void Stepper_Disable(uint8_t addr)
{
    Emm_V5_En_Control(addr, false, false);
}

/***************************************************************************
函数功能：触发步进电机回零
入口参数：addr - 电机地址, mode - 回零模式
返回值  ：无
***************************************************************************/
void Stepper_Home(uint8_t addr, uint8_t mode)
{
    Emm_V5_Origin_Modify_Params(addr,
                                false,                     /* 不存储到EEPROM */
                                mode,                      /* 回零模式 */
                                STEPPER_DIR_CW,            /* 回零方向 */
                                STEPPER_HOME_SPEED,        /* 回零速度 */
                                STEPPER_HOME_TIMEOUT,      /* 超时时间 */
                                100,                       /* 碰撞检测转速 */
                                1000,                      /* 碰撞检测电流 mA */
                                100,                       /* 碰撞检测时间 ms */
                                false);                    /* 上电不自动回零 */

    delay_ms(10);                                           /* 等待参数写入 */
    Emm_V5_Origin_Trigger_Return(addr, mode, false);
}

/***************************************************************************
函数功能：强制中断并退出回零
入口参数：addr - 电机地址
返回值  ：无
***************************************************************************/
void Stepper_Home_Interrupt(uint8_t addr)
{
    Emm_V5_Origin_Interrupt(addr);
}

/***************************************************************************
函数功能：设置当前位置为原点（并可选存储到EEPROM）
入口参数：addr - 电机地址, save - 是否存入EEPROM
返回值  ：无
***************************************************************************/
void Stepper_Set_Zero(uint8_t addr, bool save)
{
    Emm_V5_Origin_Set_O(addr, save);
}

/***************************************************************************
函数功能：清零当前位置
入口参数：addr - 电机地址
返回值  ：无
***************************************************************************/
void Stepper_Reset_CurPos(uint8_t addr)
{
    Emm_V5_Reset_CurPos_To_Zero(addr);
}

/***************************************************************************
函数功能：相对移动（走指定脉冲数）
入口参数：addr - 电机地址, dir - 方向, steps - 脉冲数
返回值  ：无
***************************************************************************/
void Stepper_Move_Relative(uint8_t addr, uint8_t dir, uint32_t steps)
{
    Emm_V5_Pos_Control(addr,
                       dir,
                       STEPPER_DEFAULT_SPEED,
                       STEPPER_DEFAULT_ACC,
                       steps,
                       false,                                /* 相对运动 */
                       false);                               /* 不同步 */
}

/***************************************************************************
函数功能：缓存相对移动命令，等待广播同步触发
入口参数：addr - 电机地址, dir - 方向, steps - 脉冲数
返回值  ：无
***************************************************************************/
void Stepper_Queue_Relative(uint8_t addr, uint8_t dir, uint32_t steps)
{
    Emm_V5_Pos_Control(addr,
                       dir,
                       STEPPER_DEFAULT_SPEED,
                       STEPPER_DEFAULT_ACC,
                       steps,
                       false,                                /* 相对运动 */
                       true);                                /* 等待同步触发 */
}

/***************************************************************************
函数功能：绝对位置移动
入口参数：addr - 电机地址, position - 绝对位置
返回值  ：无
***************************************************************************/
void Stepper_Move_Absolute(uint8_t addr, uint32_t position)
{
    Emm_V5_Pos_Control(addr,
                       STEPPER_DIR_CW,
                       STEPPER_DEFAULT_SPEED,
                       STEPPER_DEFAULT_ACC,
                       position,
                       true,                                 /* 绝对运动 */
                       false);
}

/***************************************************************************
函数功能：速度模式运行
入口参数：addr - 电机地址, dir - 方向, speed - 转速 RPM
返回值  ：无
***************************************************************************/
void Stepper_Move_Velocity(uint8_t addr, uint8_t dir, uint16_t speed)
{
    Emm_V5_Vel_Control(addr, dir, speed, STEPPER_DEFAULT_ACC, false);
}

/***************************************************************************
函数功能：立即停止电机
入口参数：addr - 电机地址
返回值  ：无
***************************************************************************/
void Stepper_Stop(uint8_t addr)
{
    Emm_V5_Stop_Now(addr, false);
}

/***************************************************************************
函数功能：触发多机同步运动
入口参数：addr - 电机地址
返回值  ：无
***************************************************************************/
void Stepper_Sync_Move(uint8_t addr)
{
    Emm_V5_Synchronous_motion(addr);
}

/***************************************************************************
函数功能：解除电机堵转保护
入口参数：addr - 电机地址
返回值  ：无
***************************************************************************/
void Stepper_Unclog(uint8_t addr)
{
    Emm_V5_Reset_Clog_Pro(addr);
}
